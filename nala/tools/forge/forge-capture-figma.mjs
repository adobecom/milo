// Runs a real Figma session against the live forge backend and saves the
// generated versions to fixtures/figma-session.json for use by the
// @forge-figma-ship Playwright test (no 1-hour wait on every run).
//
// Usage:
//   FORGE_FIGMA_TOKEN=figd_xxx FORGE_FIGMA_URL=https://... npm run nala:forge:capture
//
// Env vars:
//   FORGE_SERVER_URL      Backend origin  (default http://localhost:8080)
//   FORGE_FIGMA_URL       Figma frame URL (default: the project design frame)
//   FORGE_REPO_PATH       Repo path forwarded in serverConfig (uses REPO_PATH fallback)
//   FORGE_ACCOUNT         Bearer token / account key  (default: local-capture)
//   FORGE_SESSION_ID      Skip creation and resume polling an existing session
//
// Note: no Figma token needed here — the server uses its own FIGMA_TOKEN env var.

import { writeFileSync, mkdirSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));

const SERVER_URL = process.env.FORGE_SERVER_URL || 'http://localhost:8080';
const FIGMA_URL = process.env.FORGE_FIGMA_URL
  || 'https://www.figma.com/design/fMKQndDRSNu4yTjDYyiQoN?node-id=7-7103';
const REPO_PATH = process.env.FORGE_REPO_PATH || process.env.REPO_PATH || '';
const ACCOUNT = process.env.FORGE_ACCOUNT || 'local-capture';
const RESUME_SESSION_ID = process.env.FORGE_SESSION_ID || '';

// Statuses that mean generation is still in progress.
const IN_PROGRESS = new Set(['pending', 'generating', 'running']);

const FIXTURE_DIR = resolve(__dirname, 'fixtures');
const FIXTURE_PATH = resolve(FIXTURE_DIR, 'figma-session.json');

const POLL_INTERVAL_MS = 30_000;
const TIMEOUT_MS = 90 * 60 * 1_000;

async function apiFetch(path, init = {}) {
  const url = `${SERVER_URL}/forge${path}`;
  const res = await fetch(url, {
    ...init,
    headers: {
      'content-type': 'application/json',
      authorization: `Bearer ${ACCOUNT}`,
      ...(init.headers || {}),
    },
  });
  const text = await res.text();
  let body;
  try { body = JSON.parse(text); } catch { body = { _raw: text }; }
  if (!res.ok) throw new Error(`HTTP ${res.status}: ${JSON.stringify(body)}`);
  return body;
}

async function poll(sessionId) {
  const deadline = Date.now() + TIMEOUT_MS;
  while (Date.now() < deadline) {
    const session = await apiFetch(`/sessions/${sessionId}`);
    const { status } = session;
    const ts = new Date().toTimeString().slice(0, 8);
    process.stdout.write(`  [${ts}] status=${status}  versions=${session.versions?.length ?? 0}\n`);
    if (!IN_PROGRESS.has(status)) return session;
    await new Promise((r) => setTimeout(r, POLL_INTERVAL_MS));
  }
  throw new Error(`Timed out after ${TIMEOUT_MS / 60_000} min waiting for session ${sessionId}`);
}

async function main() {
  console.log('forge-capture-figma');
  console.log(`  server : ${SERVER_URL}`);
  console.log(`  account: ${ACCOUNT}`);
  console.log('');

  let sessionId = RESUME_SESSION_ID;

  if (sessionId) {
    console.log(`Resuming existing session: ${sessionId}`);
  } else {
    console.log(`  figma  : ${FIGMA_URL}`);
    console.log('Creating session...');
    const session = await apiFetch('/sessions', {
      method: 'POST',
      body: JSON.stringify({
        source: 'figma',
        sourceInput: { figmaUrl: FIGMA_URL },
        daContext: null,
        serverConfig: { repoPath: REPO_PATH },
      }),
    });
    sessionId = session.sessionId || session.id;
    console.log(`Session started: ${sessionId}`);
  }

  console.log('Polling (Figma generation takes ~45-90 min)...\n');

  const done = await poll(sessionId);

  if (done.status === 'error') {
    console.error('\nSession failed. Last messages:');
    (done.messages || []).slice(-5).forEach((m) => console.error(' ', m));
    process.exit(1);
  }

  const fixture = {
    _note: 'Generated by forge-capture-figma.mjs — run `npm run nala:forge:capture` to refresh.',
    sessionId,
    source: done.source,
    sourceInput: done.sourceInput,
    versions: done.versions,
  };

  mkdirSync(FIXTURE_DIR, { recursive: true });
  writeFileSync(FIXTURE_PATH, JSON.stringify(fixture, null, 2));

  console.log(`\nFixture saved → ${FIXTURE_PATH}`);
  console.log(`  status  : ${done.status}`);
  console.log(`  versions: ${done.versions?.length}`);
  console.log('');
  console.log('Next: run the ship-only test');
  console.log('  FORGE_TEST_MODE=real npm run nala:forge:real');
}

main().catch((e) => {
  console.error(e.message);
  process.exit(1);
});
